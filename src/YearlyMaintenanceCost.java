public class YearlyMaintenanceCost {
    public static void main(String[] args) {
        //Declared and initialized variables
        int summerCost = 100;
        int fallCost = 150;
        int winterCost = 200;
        int springCost = 50;
        int totalCost = summerCost + fallCost + winterCost + springCost;
        //outputed all seasonal costs and total cost
        System.out.println("Your summer cost is " + summerCost);
        System.out.println("Your fall cost is " + fallCost);
        System.out.println("Your winter cost is " + winterCost);
        System.out.println("Your spring cost is " + springCost);
        System.out.println("Your total cost is " + totalCost);

    }
}